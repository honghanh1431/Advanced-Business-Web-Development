  // Tính số ngày trong tháng âm lịch
  private leapDays(y: number): number {
    if (this.leapMonth(y)) {
      return (this.lunarInfo[y - 1900] & 0x10000) ? 30 : 29;
    } else {
      return 0;
    }
  }

  // Tìm tháng nhuận trong năm
  private leapMonth(y: number): number {
    return this.lunarInfo[y - 1900] & 0xf;
  }

  // Tính tổng số ngày trong năm âm lịch
  private lYearDays(y: number): number {
    let sum = 348;
    for (let i = 0x8000; i > 0x8; i >>= 1) {
      sum += (this.lunarInfo[y - 1900] & i) ? 1 : 0;
    }
    return sum + this.leapDays(y);
  }

  // Số ngày trong tháng thường
  private monthDays(y: number, m: number): number {
    return (this.lunarInfo[y - 1900] & (0x10000 >> m)) ? 30 : 29;
  }

  // Chuyển đổi dương sang âm
  private solar2lunar(y: number, m: number, d: number): any {
    const date = new Date(y, m - 1, d);
    const baseDate = new Date(1900, 0, 31);
    let offset = Math.floor((date.getTime() - baseDate.getTime() + 7 * 3600000) / 86400000);

    let temp = 0;
    let lunarYear = 1900;
    for (; lunarYear < 2101 && offset > 0; lunarYear++) {
      temp = this.lYearDays(lunarYear);
      offset -= temp;
    }
    if (offset < 0) {
      offset += temp;
      lunarYear--;
    }

    let leap = this.leapMonth(lunarYear);
    let isLeap = false;
    let lunarMonth = 1;
    for (; lunarMonth < 13 && offset > 0; lunarMonth++) {
      if (leap > 0 && lunarMonth === (leap + 1) && !isLeap) {
        --lunarMonth;
        isLeap = true;
        temp = this.leapDays(lunarYear);
      } else {
        temp = this.monthDays(lunarYear, lunarMonth);
      }

      if (isLeap && lunarMonth === (leap + 1)) {
        isLeap = false;
      }
      offset -= temp;
    }

    if (offset === 0 && leap > 0 && lunarMonth === leap + 1) {
      if (isLeap) {
        isLeap = false;
      } else {
        isLeap = true;
        --lunarMonth;
      }
    }

    if (offset < 0) {
      offset += temp;
      --lunarMonth;
    }

    return {
      year: lunarYear,
      month: lunarMonth,
      day: offset + 1,
      isLeap: isLeap
    };
  }

  // Tính Can Chi
  private getCanChiYear(y: number): string {
    return this.CAN[(y + 6) % 10] + ' ' + this.CHI[(y + 8) % 12];
  }

  private getCanChiMonth(m: number, y: number): string {
    return this.CAN[(y * 12 + m + 3) % 10] + ' ' + this.CHI[(m + 1) % 12];
  }

  private getCanChiDay(date: Date): string {
    const baseDate = new Date(1900, 0, 1);
    const offset = Math.floor((date.getTime() - baseDate.getTime()) / 86400000);
    return this.CAN[(offset + 10) % 10] + ' ' + this.CHI[offset % 12];
  }

